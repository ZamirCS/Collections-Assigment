name = input('Enter name\n')

gradeList = [int(i) for i in input("Enter grades:\n").split()]

def avarage():
    gradeAvarge = sum(gradeList)/len(gradeList)
    return gradeAvarge

def letter():
    if avarage() >= 90:
        letterGrade = 'A'
    elif avarage() >= 80:
        letterGrade = 'B'
    elif avarage() >= 70:
        letterGrade = 'C'
    elif avarage() >= 60:
        letterGrade = 'D'
    else:
        letterGrade = 'F'
    return letterGrade



print(name)
print('Average:',round(avarage(),1))
print('Letter Grade:',letter())
print()

